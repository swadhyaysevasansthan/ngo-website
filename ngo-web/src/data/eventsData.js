// Helper function to get unique states
export const getUniqueStates = () => {
  const states = pastEvents.map(event => event.state);
  return [...new Set(states)].sort();
};

// Helper function to filter events by state
export const filterEventsByState = (state) => {
  if (!state || state === 'all') return pastEvents;
  return pastEvents.filter(event => event.state === state);
};

// Helper function to get event count by state
export const getEventCountByState = () => {
  return pastEvents.reduce((acc, event) => {
    acc[event.state] = (acc[event.state] || 0) + 1;
    return acc;
  }, {});
};

// Helper function to filter by multiple states
export const filterEventsByStates = (states) => {
  if (!states || states.length === 0 || states.includes('all')) return pastEvents;
  return pastEvents.filter(event => states.includes(event.state));
};

// Helper function to get events by city
export const filterEventsByCity = (city) => {
  if (!city || city === 'all') return pastEvents;
  return pastEvents.filter(event => event.city.toLowerCase() === city.toLowerCase());
};
